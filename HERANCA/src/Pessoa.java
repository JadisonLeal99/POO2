import java.time.LocalDate;

public class Pessoa {
	String nome;
	String sobrenome;
	String endereco;
	LocalDate dataNascimento;
	public Pessoa(String nome, String sobrenome, String endereco, LocalDate dataNascimento) {
		this.nome = nome;
		this.sobrenome = sobrenome;
		this.endereco = endereco;
		this.dataNascimento = dataNascimento;
	}
	
	
	
}
